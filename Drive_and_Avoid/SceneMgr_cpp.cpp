#include "DxLib.h"
#include"Game.h"
#include"Menu.h"
#include "Help.h"
#include "End.h"
#include "SceneMgr.h"

static eScene mScene = eScene_Menu; //シーン管理変数
static eScene mNextScene = eScene_None; //次のシーン管理変数

static void SceneMgr_InitializeModule(eScene scene);//指定モジュールを初期化する
static void SceneMgr_FinalizeModule(eScene scene);//指定モジュールの終了処理を行う

//初期化
void SceneMgr_Initialize() {
	SceneMgr_InitializeModule(mScene);
}

//終了処理
void SceneMgr_Finalize() {
	SceneMgr_FinalizeModule(mScene);
}

//更新
void SceneMgr_Update() {
	if (mNextScene != eScene_None) {//次のシーンがセットされていたら
		SceneMgr_FinalizeModule(mScene);//現在のシーンの終了処理を実行
		mScene = mNextScene;//次のシーンを現在のシーンセット
		mNextScene = eScene_None;  //次のシーン情報をクリア
		SceneMgr_InitializeModule(mScene);  //現在のシーンを初期化
	}
	switch (mScene) {
	case eScene_Menu:
		Menu_Update();
		break;
	case eScene_Game:
		Game_Update();
		break;
	case eScene_Help:
		Help_Update();
		break;
	case eScene_End:
		break;
}
}

//描画
void SceneMgr_Draw() {
	switch (mScene) {
	case eScene_Menu:
		Menu_Draw();
		break;
	case eScene_Game:
		Game_Draw();
		break;
	case eScene_Help:
		Help_Draw();
		break;
	case eScene_End:
		End_Draw();
		break;
	}
}

//引数nextSceneにシーンを変更する
void SceneMgr_ChangeScene(eScene NextScene) {
	mNextScene = NextScene; //次のシーンをセットする
}

//引数sceneモジュールを初期化する
static void SceneMgr_InitializeModule(eScene scene) {
	switch (scene) {
	case eScene_Menu:
			Menu_Initialize();
			break;
	case eScene_Game:
			Game_Initialize();
			break;
	case eScene_Help:
		Help_Initialize();
		break;
	case eScene_End:
		End_Initialize();
		break;
	}
}

//引数sceneモジュールの終了処理を行う
static void SceneMgr_FinalizeModule(eScene scene) {
	switch (scene) {
	case eScene_Menu:
			Menu_Finalize();
			break;
	case eScene_Game:
			Game_Finalize();
			break;
	case eScene_Help:
		Help_Finalize();
		break;
	case eScene_End:
		End_Finalize();
		break;
	}
}


int SceneMgr_GameEnd_Status() {
	if (mScene == eScene_End) {
		return 1;
	}
	else {
		return 0;
	}
}